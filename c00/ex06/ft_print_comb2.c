#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	char	n[5];

	i = -1;
	while (4 > i++)
		n[i] = i != 2 ? '0' - 1 : ' ';
	while (++n[0] <= '9')
	{
		n[1] = '0' - 1;
		while (++n[1] <= '8' || (n[1] == '9' && n[0] < '9'))
		{
			n[3] = n[0] - 1;
			while ('9' > n[3]++)
			{
				n[4] = n[3] == n[0] ? n[1] : '0' - 1;
				while ('9' > n[4]++)
				{
					write(1, n, 5);
					if (n[0] != '9' || n[1] != '8'
							|| n[3] != '9' || n[4] != '9')
						write(1, ", ", 2);
				}
			}
		}
	}
}
